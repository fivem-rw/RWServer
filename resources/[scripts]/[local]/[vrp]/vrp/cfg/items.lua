-- load more items function
local function load_item_pack(name)
  local items = module("cfg/item/" .. name)
  if items then
    for k, v in pairs(items) do
      cfg.items[k] = v
    end
  else
    print("[vRP] item pack [" .. name .. "] not found")
  end
end

